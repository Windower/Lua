function buffIDtoName( id )
	for k,v in pairs( buffIDs ) do
		if (v == id) then
			return k;
		end	
	end
end

function buffNameToID( buffName )
	for k,v in pairs( buffIDs ) do
		if (k == buffName) then
			return v;
		end	
	end
end	

function isBuffActive( buffName )
	local self = get_player()
	for k,v in pairs( self.buffs ) do
		if ((v ~= 65535) and (v == buffNameToID(buffName))) then
			return true;
		end	
	end
	return false;
end